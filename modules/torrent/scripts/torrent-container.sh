#!/bin/bash
# =============================================================================
# TORRENT-CONTAINER - Container setup, config, and lifecycle management
# =============================================================================
# Creates and manages Gluetun + qBittorrent Podman containers with
# configurable VPN provider, protocol, and Podman secrets for credentials.
#
# Commands:
#   install  - Interactive setup (provider, credentials, countries)
#   info     - Show current configuration and container status
#   edit     - Modify config interactively, recreate containers
#   update   - Pull latest images and recreate if updated
#   delete   - Remove containers, secrets, and config
#
# Module: torrent
# Requires: core, config, ui
# Version: 0.1.0
#
# Usage:
#   torrent-container                    # Interactive menu
#   torrent-container install            # Full interactive setup
#   torrent-container info               # Show current config
#   torrent-container edit               # Modify and recreate
#   torrent-container update             # Pull + recreate images
#   torrent-container delete             # Remove everything
# =============================================================================

set -euo pipefail
trap 'echo "ERROR: torrent-container failed at line $LINENO" >&2; exit 1' ERR

# ===================
# Load shared library
# ===================
readonly LIB_DIR="/usr/local/lib/system-scripts"
source "$LIB_DIR/core.sh"
source "$LIB_DIR/config.sh"
source "$LIB_DIR/ui.sh"

# =============================================================================
# CONSTANTS
# =============================================================================
readonly IMAGE_GLUETUN="docker.io/qmcgaw/gluetun"
readonly IMAGE_QBITTORRENT="lscr.io/linuxserver/qbittorrent:latest"
readonly CONTAINER_CONF="${TORRENT_CONTAINER_CONF:-$HOME/.config/torrent/container.conf}"
readonly CONTAINER_DATA_DIR="${TORRENT_DATA_DIR:-$HOME/.config/podman/containers}"

ACTION="${1:-}"

# ===================
# Provider data
# ===================

# Credential patterns:
#   openvpn_user_pass     - username + password (most providers)
#   openvpn_user_only     - account ID only (mullvad)
#   wireguard_key         - private key only
#   wireguard_key_addr    - private key + address (mullvad, surfshark)
declare -A PROVIDER_CREDS=(
    ["protonvpn:openvpn"]="openvpn_user_pass"
    ["protonvpn:wireguard"]="wireguard_key"
    ["mullvad:openvpn"]="openvpn_user_only"
    ["mullvad:wireguard"]="wireguard_key_addr"
    ["nordvpn:openvpn"]="openvpn_user_pass"
    ["nordvpn:wireguard"]="wireguard_key"
    ["surfshark:openvpn"]="openvpn_user_pass"
    ["surfshark:wireguard"]="wireguard_key_addr"
)

declare -A PROVIDER_HELP=(
    ["protonvpn:openvpn"]="https://account.protonvpn.com/account#openvpn"
    ["protonvpn:wireguard"]="https://account.protonvpn.com/downloads#wireguard-configuration"
    ["mullvad:openvpn"]="https://mullvad.net/en/account"
    ["mullvad:wireguard"]="https://mullvad.net/en/account/wireguard-config"
    ["nordvpn:openvpn"]="https://my.nordaccount.com/dashboard/nordvpn/manual-configuration/"
    ["nordvpn:wireguard"]="https://my.nordaccount.com/dashboard/nordvpn/manual-configuration/"
    ["surfshark:openvpn"]="https://my.surfshark.com/vpn/manual-setup"
    ["surfshark:wireguard"]="https://my.surfshark.com/vpn/manual-setup/main/wireguard"
)

# Providers that support FREE_ONLY toggle
readonly FREE_ONLY_PROVIDERS="protonvpn"

# All supported provider names (for ui_choose)
readonly PROVIDERS="protonvpn mullvad nordvpn surfshark"

# =============================================================================
# CONFIG FUNCTIONS
# =============================================================================

load_container_config() {
    if [[ ! -f "$CONTAINER_CONF" ]]; then
        return 1
    fi
    # shellcheck source=/dev/null
    source "$CONTAINER_CONF"
}

save_container_config() {
    local provider="$1" vpn_type="$2" countries="$3" free_only="$4"
    local tz="$5" puid="$6" pgid="$7"
    local wg_addresses="${8:-}" api_key="${9:-}"

    mkdir -p "$(dirname "$CONTAINER_CONF")"

    {
        printf '# Torrent container configuration\n'
        printf '# Generated by torrent-container on %s\n' "$(date '+%Y-%m-%d %H:%M:%S')"
        printf 'PROVIDER=%s\n' "$provider"
        printf 'VPN_TYPE=%s\n' "$vpn_type"
        printf 'COUNTRIES="%s"\n' "$countries"
        printf 'FREE_ONLY=%s\n' "$free_only"
        printf 'TZ=%s\n' "$tz"
        printf 'PUID=%s\n' "$puid"
        printf 'PGID=%s\n' "$pgid"
        if [[ -n "$wg_addresses" ]]; then
            printf 'WIREGUARD_ADDRESSES=%s\n' "$wg_addresses"
        fi
        printf 'GLUETUN_API_KEY=%s\n' "$api_key"
    } > "$CONTAINER_CONF"

    chmod 600 "$CONTAINER_CONF"
}

# =============================================================================
# PODMAN SECRET FUNCTIONS
# =============================================================================

secret_set() {
    local name="$1"
    local value="$2"
    podman secret rm "$name" 2>/dev/null || true
    printf '%s' "$value" | podman secret create "$name" -
}

secret_exists() {
    podman secret inspect "$1" &>/dev/null
}

secret_remove() {
    podman secret rm "$1" 2>/dev/null || true
}

secrets_remove_all() {
    local names=(
        torrent-openvpn-user torrent-openvpn-password
        torrent-wireguard-key torrent-wireguard-psk
    )
    for name in "${names[@]}"; do
        secret_remove "$name"
    done
}

# Return space-separated list of secret names needed for a provider+type
secrets_needed() {
    local provider="$1"
    local vpn_type="$2"
    local key="${provider}:${vpn_type}"
    local cred="${PROVIDER_CREDS[$key]:-}"

    case "$cred" in
        openvpn_user_pass)  echo "torrent-openvpn-user torrent-openvpn-password" ;;
        openvpn_user_only)  echo "torrent-openvpn-user" ;;
        wireguard_key)      echo "torrent-wireguard-key" ;;
        wireguard_key_addr) echo "torrent-wireguard-key" ;;
        *) error "Unknown credential pattern for $key" "exit" ;;
    esac
}

# Build --secret flags for podman run (one element per line for array reading)
build_secret_flags() {
    local provider="$1"
    local vpn_type="$2"
    local key="${provider}:${vpn_type}"
    local cred="${PROVIDER_CREDS[$key]:-}"

    case "$cred" in
        openvpn_user_pass)
            echo "--secret"
            echo "torrent-openvpn-user,type=env,target=OPENVPN_USER"
            echo "--secret"
            echo "torrent-openvpn-password,type=env,target=OPENVPN_PASSWORD"
            ;;
        openvpn_user_only)
            echo "--secret"
            echo "torrent-openvpn-user,type=env,target=OPENVPN_USER"
            ;;
        wireguard_key)
            echo "--secret"
            echo "torrent-wireguard-key,type=env,target=WIREGUARD_PRIVATE_KEY"
            ;;
        wireguard_key_addr)
            echo "--secret"
            echo "torrent-wireguard-key,type=env,target=WIREGUARD_PRIVATE_KEY"
            ;;
    esac
}

# =============================================================================
# CONTAINER FUNCTIONS
# =============================================================================

container_exists() {
    podman ps -a --format "{{.Names}}" 2>/dev/null | grep -q "^${1}$"
}

container_running() {
    podman ps --format "{{.Names}}" 2>/dev/null | grep -q "^${1}$"
}

remove_containers() {
    for ct in qbittorrent gluetun; do
        if container_exists "$ct"; then
            podman stop -t 10 "$ct" 2>/dev/null || true
            podman rm "$ct" 2>/dev/null || true
        fi
    done
}

# =============================================================================
# HEALTH CHECK FUNCTIONS
# =============================================================================

# Poll gluetun's control server for VPN readiness
# Uses /v1/publicip/ip which works for both openvpn and wireguard
poll_gluetun_health() {
    local max_wait="${1:-120}"
    local interval=3
    local elapsed=0

    while [[ $elapsed -lt $max_wait ]]; do
        local response
        response=$(curl -s --max-time 3 \
            -H "X-API-Key: ${GLUETUN_API_KEY:-}" \
            "http://localhost:18001/v1/publicip/ip" 2>/dev/null) || true
        if echo "$response" | grep -q '"public_ip"' 2>/dev/null; then
            echo ""
            return 0
        fi
        echo -n "."
        sleep "$interval"
        ((elapsed += interval))
    done
    echo ""
    return 1
}

# Poll for container running state
poll_container_running() {
    local name="$1"
    local max_wait="${2:-30}"
    local elapsed=0

    while [[ $elapsed -lt $max_wait ]]; do
        if container_running "$name"; then
            return 0
        fi
        sleep 1
        ((elapsed++))
    done
    return 1
}

# =============================================================================
# CONTAINER CREATION — Single source of truth
# =============================================================================

create_containers() {
    # Load paths (DOWNLOAD_DIR from paths.conf, derive TORRENT_DIR)
    load_config
    TORRENT_DIR="$DOWNLOAD_DIR/torrents"

    # Load container config
    if ! load_container_config; then
        error "Container configuration not found: $CONTAINER_CONF" "exit"
    fi

    # --- Build gluetun env vars ---
    # shellcheck disable=SC2153  # Variables assigned by load_container_config
    local gluetun_env=(
        -e "VPN_SERVICE_PROVIDER=$PROVIDER"
        -e "VPN_TYPE=$VPN_TYPE"
        -e "SERVER_COUNTRIES=$COUNTRIES"
        -e "FREE_ONLY=$FREE_ONLY"
        -e "DNS_SERVER=off"
        -e "DNS_ADDRESS=1.1.1.1"
        -e "DNS_FALLBACK_ADDRESS=8.8.8.8"
        -e "HTTPPROXY=off"
        -e "SHADOWSOCKS=off"
        -e "HTTP_CONTROL_SERVER_ADDRESS=:18001"
        -e "HTTP_CONTROL_SERVER_AUTH_DEFAULT_ROLE={\"auth\":\"apikey\",\"apikey\":\"$GLUETUN_API_KEY\"}"
        -e "FIREWALL=on"
        -e "FIREWALL_VPN_INPUT_PORTS=6881"
    )

    # WireGuard addresses (required by some providers)
    if [[ "$VPN_TYPE" == "wireguard" && -n "${WIREGUARD_ADDRESSES:-}" ]]; then
        gluetun_env+=(-e "WIREGUARD_ADDRESSES=$WIREGUARD_ADDRESSES")
    fi

    # --- Build secret flags ---
    local secret_flags=()
    while IFS= read -r line; do
        [[ -n "$line" ]] && secret_flags+=("$line")
    done < <(build_secret_flags "$PROVIDER" "$VPN_TYPE")

    # --- Remove leftover auth config file (replaced by env var) ---
    rm -f "$CONTAINER_DATA_DIR/gluetun/auth/config.toml"

    # --- Create gluetun ---
    info "Creating gluetun container..."
    podman run -d \
        --name gluetun \
        --privileged \
        --cap-add NET_ADMIN \
        --device /dev/net/tun:/dev/net/tun \
        "${gluetun_env[@]}" \
        "${secret_flags[@]}" \
        -p 18002:18002 \
        -p 18001:18001 \
        -p 6881:6881 \
        -p 6881:6881/udp \
        -v "$CONTAINER_DATA_DIR/gluetun:/gluetun:Z" \
        --restart unless-stopped \
        "$IMAGE_GLUETUN"

    # --- Wait for gluetun VPN connection ---
    info "Waiting for VPN connection..."
    if ! poll_gluetun_health 120; then
        error "Gluetun failed to connect within 120s"
        echo -e "  Check logs: ${C_CYAN}podman logs gluetun${C_NC}"
        return 1
    fi
    success "Gluetun VPN connected"

    # --- Create qbittorrent ---
    info "Creating qBittorrent container..."
    # shellcheck disable=SC2153  # Variables assigned by load_container_config
    podman run -d \
        --name qbittorrent \
        --network container:gluetun \
        -e "PUID=$PUID" \
        -e "PGID=$PGID" \
        -e "TZ=$TZ" \
        -e WEBUI_PORT=18002 \
        -v "$TORRENT_DIR:/downloads:Z" \
        -v "$CONTAINER_DATA_DIR/qbittorrent:/config:Z" \
        --restart unless-stopped \
        "$IMAGE_QBITTORRENT"

    # --- Wait for qbittorrent to start ---
    if ! poll_container_running "qbittorrent" 30; then
        error "qBittorrent failed to start within 30s"
        return 1
    fi
    success "qBittorrent container created"
}

# =============================================================================
# INTERACTIVE PROMPT FUNCTIONS
# =============================================================================

prompt_provider() {
    # shellcheck disable=SC2086
    ui_choose --header "Select VPN provider" $PROVIDERS
}

prompt_vpn_type() {
    local provider="$1"
    local types=()

    [[ -n "${PROVIDER_CREDS["$provider:openvpn"]:-}" ]] && types+=("openvpn")
    [[ -n "${PROVIDER_CREDS["$provider:wireguard"]:-}" ]] && types+=("wireguard")

    if [[ ${#types[@]} -eq 1 ]]; then
        echo "${types[0]}"
        return
    fi

    ui_choose --header "VPN protocol" "${types[@]}"
}

# Curated list of torrent-friendly countries (privacy laws, no data retention)
readonly TORRENT_COUNTRIES=(Netherlands Switzerland Sweden Romania Spain Canada)

prompt_countries() {
    local default="${1:-}"
    local selected=()

    if has_gum; then
        # Multi-select with gum (space to toggle, enter to confirm)
        mapfile -t selected < <(
            gum choose --no-limit \
                --header "Server countries (space to select, enter to confirm)" \
                "${TORRENT_COUNTRIES[@]}" "Other"
        )
    else
        # Fallback: numbered list, multiple numbers separated by spaces
        echo -e "  ${C_BOLD}Server countries${C_NC} (enter numbers separated by spaces):" >&2
        local i=1
        for country in "${TORRENT_COUNTRIES[@]}" "Other"; do
            echo -e "    ${C_BOLD}$i)${C_NC} $country" >&2
            ((i++))
        done
        local input
        read -r -p "  Selection: " input
        local total=$(( ${#TORRENT_COUNTRIES[@]} + 1 ))
        for num in $input; do
            if [[ "$num" =~ ^[0-9]+$ ]] && (( num >= 1 && num <= total )); then
                if (( num <= ${#TORRENT_COUNTRIES[@]} )); then
                    selected+=("${TORRENT_COUNTRIES[$((num-1))]}")
                else
                    selected+=("Other")
                fi
            fi
        done
    fi

    # Check if "Other" was selected
    local has_other=false
    local countries=()
    for item in "${selected[@]}"; do
        if [[ "$item" == "Other" ]]; then
            has_other=true
        else
            countries+=("$item")
        fi
    done

    if [[ "$has_other" == true ]]; then
        # Pre-fill with selected countries from list (or default)
        local prefill=""
        if [[ ${#countries[@]} -gt 0 ]]; then
            prefill=$(IFS=,; echo "${countries[*]}")
        else
            prefill="$default"
        fi
        local result
        result=$(ui_input "Server countries (comma-separated)" "$prefill")
        echo "${result:-Netherlands}"
    elif [[ ${#countries[@]} -gt 0 ]]; then
        IFS=,; echo "${countries[*]}"
    else
        echo "${default:-Netherlands}"
    fi
}

prompt_free_only() {
    local provider="$1"
    if [[ "$FREE_ONLY_PROVIDERS" == *"$provider"* ]]; then
        ui_choose --header "Use free servers only?" "off" "on"
    else
        echo "off"
    fi
}

# Common European timezones + UTC
readonly TIMEZONES="Europe/Amsterdam Europe/Paris Europe/London Europe/Berlin Europe/Brussels Europe/Zurich Europe/Rome Europe/Madrid UTC"

detect_timezone() {
    local default="${1:-}"

    # Auto-detect system timezone if no default provided
    if [[ -z "$default" && -f /etc/localtime ]]; then
        default=$(readlink -f /etc/localtime 2>/dev/null \
            | sed 's|.*/zoneinfo/||') || true
    fi
    [[ -z "$default" ]] && default="UTC"

    # Build options: curated list + Other
    local choice
    # shellcheck disable=SC2086  # Intentional word splitting for menu options
    choice=$(ui_choose --header "Timezone" $TIMEZONES "Other")

    if [[ "$choice" == "Other" ]]; then
        local result
        result=$(ui_input "Timezone (e.g. America/New_York)" "$default")
        echo "${result:-$default}"
    else
        echo "$choice"
    fi
}

prompt_and_store_credentials() {
    local provider="$1"
    local vpn_type="$2"
    local key="${provider}:${vpn_type}"
    local cred="${PROVIDER_CREDS[$key]:-}"
    local help_url="${PROVIDER_HELP[$key]:-}"

    echo ""
    info "Credentials for $provider ($vpn_type)"
    if [[ -n "$help_url" ]]; then
        echo -e "  Get them at: ${C_CYAN}$help_url${C_NC}"
    fi
    echo ""

    case "$cred" in
        openvpn_user_pass)
            local user pass
            user=$(ui_input "OpenVPN Username")
            [[ -z "$user" ]] && error "Username cannot be empty" "exit"
            pass=$(ui_password "OpenVPN Password")
            [[ -z "$pass" ]] && error "Password cannot be empty" "exit"
            secret_set "torrent-openvpn-user" "$user"
            secret_set "torrent-openvpn-password" "$pass"
            ;;
        openvpn_user_only)
            local user
            user=$(ui_input "Account ID")
            [[ -z "$user" ]] && error "Account ID cannot be empty" "exit"
            secret_set "torrent-openvpn-user" "$user"
            ;;
        wireguard_key)
            local key_val
            key_val=$(ui_password "WireGuard Private Key")
            [[ -z "$key_val" ]] && error "Private key cannot be empty" "exit"
            secret_set "torrent-wireguard-key" "$key_val"
            ;;
        wireguard_key_addr)
            local key_val addr
            key_val=$(ui_password "WireGuard Private Key")
            [[ -z "$key_val" ]] && error "Private key cannot be empty" "exit"
            addr=$(ui_input "WireGuard Address (e.g. 10.64.222.21/32)")
            [[ -z "$addr" ]] && error "Address cannot be empty" "exit"
            secret_set "torrent-wireguard-key" "$key_val"
            # Address stored in config, not as secret (not sensitive)
            WG_ADDR_RESULT="$addr"
            ;;
        *)
            error "Unknown credential pattern for $key" "exit"
            ;;
    esac

    success "Credentials stored as Podman secrets"
}

