# ===================
# install
# ===================
cmd_install() {
    ui_header "Gluetun + qBittorrent Container Setup"

    # Check if containers already exist
    if container_exists "gluetun" || container_exists "qbittorrent"; then
        warn "Containers already exist."
        if ui_confirm "Remove existing containers and reconfigure?"; then
            remove_containers
        else
            info "Use 'torrent-container edit' to modify config."
            return 0
        fi
    fi

    # Load paths config (DOWNLOAD_DIR, EXPORT_DIR from paths.conf)
    load_config
    TORRENT_DIR="$DOWNLOAD_DIR/torrents"
    info "Using paths:"
    echo "  Torrent dir: $TORRENT_DIR"
    echo "  Export dir:  $EXPORT_DIR"
    echo ""

    # Create data directories
    mkdir -p "$TORRENT_DIR"
    mkdir -p "$CONTAINER_DATA_DIR/qbittorrent"
    mkdir -p "$CONTAINER_DATA_DIR/gluetun"

    # --- Interactive configuration ---
    local provider vpn_type countries free_only tz puid pgid
    provider=$(prompt_provider)
    vpn_type=$(prompt_vpn_type "$provider")

    # Store credentials as Podman secrets
    WG_ADDR_RESULT=""
    prompt_and_store_credentials "$provider" "$vpn_type"

    countries=$(prompt_countries)
    free_only=$(prompt_free_only "$provider")
    tz=$(detect_timezone "")
    puid=$(id -u)
    pgid=$(id -g)

    # Generate API key for Gluetun control server auth
    local api_key
    api_key=$(head -c 32 /dev/urandom | base64 | tr -dc 'a-zA-Z0-9' | head -c 32)

    # Save config
    save_container_config "$provider" "$vpn_type" "$countries" \
        "$free_only" "$tz" "$puid" "$pgid" "$WG_ADDR_RESULT" "$api_key"
    success "Configuration saved: $CONTAINER_CONF"

    # Create containers
    echo ""
    create_containers

    # Show qBittorrent logs for initial password
    echo ""
    podman logs qbittorrent 2>&1 | tail -20

    ui_banner "qBittorrent is running!" \
        "Web UI: http://localhost:18002" \
        "" \
        "IMPORTANT: Change the default password NOW!" \
        "  1. Open http://localhost:18002" \
        "  2. Login with temporary credentials shown in logs above" \
        "  3. Go to Settings > Web UI > Change password"

    ui_confirm "Press Enter once you have changed the password..." || true

    # Stop containers for setup completion
    echo ""
    info "Stopping containers for first-time setup completion..."
    podman stop qbittorrent gluetun 2>/dev/null || true

    # Final banner
    load_config
    TORRENT_DIR="$DOWNLOAD_DIR/torrents"
    ui_banner "Setup complete!" \
        "Detected directories:" \
        "  Torrent downloads: $TORRENT_DIR" \
        "  Export destination: $EXPORT_DIR" \
        "" \
        "Usage:" \
        "  torrent start    # Start VPN + qBittorrent" \
        "  torrent status   # Check status" \
        "  torrent stop     # Stop everything" \
        "" \
        "  torrent list     # List downloaded files" \
        "  torrent move 1   # Move file to $EXPORT_DIR" \
        "  torrent export   # Move all clean files" \
        "" \
        "  sudo quarantine  # Manage quarantined files" \
        "  torrent --help   # More options"
}

# ===================
# info
# ===================
cmd_info() {
    if ! load_container_config; then
        error "No configuration found. Run 'torrent-container install' first." "exit"
    fi

    ui_header "Torrent Container Configuration"

    echo -e "  ${C_BOLD}Provider:${C_NC}     $PROVIDER"
    echo -e "  ${C_BOLD}VPN Type:${C_NC}     $VPN_TYPE"
    echo -e "  ${C_BOLD}Countries:${C_NC}    $COUNTRIES"
    echo -e "  ${C_BOLD}Free Only:${C_NC}    $FREE_ONLY"
    echo -e "  ${C_BOLD}Timezone:${C_NC}     $TZ"
    echo -e "  ${C_BOLD}PUID/PGID:${C_NC}   $PUID/$PGID"
    if [[ -n "${WIREGUARD_ADDRESSES:-}" ]]; then
        echo -e "  ${C_BOLD}WG Address:${C_NC}   $WIREGUARD_ADDRESSES"
    fi
    echo ""

    # Secrets status
    echo -e "  ${C_BOLD}Credentials:${C_NC}"
    local needed
    needed=$(secrets_needed "$PROVIDER" "$VPN_TYPE")
    for secret_name in $needed; do
        if secret_exists "$secret_name"; then
            echo -e "    ${C_GREEN}*${C_NC} $secret_name"
        else
            echo -e "    ${C_RED}!${C_NC} $secret_name ${C_RED}(missing)${C_NC}"
        fi
    done
    echo ""

    # Container status
    echo -e "  ${C_BOLD}Containers:${C_NC}"
    for ct in gluetun qbittorrent; do
        if container_running "$ct"; then
            echo -e "    ${C_GREEN}* Running${C_NC}      $ct"
        elif container_exists "$ct"; then
            echo -e "    ${C_YELLOW}o Stopped${C_NC}      $ct"
        else
            echo -e "    ${C_RED}! Not created${C_NC}  $ct"
        fi
    done
    echo ""
}

# ===================
# edit
# ===================
cmd_edit() {
    if ! load_container_config; then
        error "No configuration found. Run 'torrent-container install' first." "exit"
    fi

    ui_header "Edit Container Configuration"

    # Save original values for comparison
    local orig_provider="$PROVIDER"
    local orig_vpn_type="$VPN_TYPE"

    # --- Provider ---
    local new_provider
    # shellcheck disable=SC2086
    new_provider=$(ui_choose --header "VPN Provider (current: $PROVIDER)" \
        $PROVIDERS "keep current")
    [[ "$new_provider" == "keep current" ]] && new_provider="$PROVIDER"

    # --- VPN Type ---
    local new_vpn_type
    local types=()
    [[ -n "${PROVIDER_CREDS["$new_provider:openvpn"]:-}" ]] && types+=("openvpn")
    [[ -n "${PROVIDER_CREDS["$new_provider:wireguard"]:-}" ]] && types+=("wireguard")
    types+=("keep current")
    new_vpn_type=$(ui_choose --header "VPN Type (current: $VPN_TYPE)" "${types[@]}")
    [[ "$new_vpn_type" == "keep current" ]] && new_vpn_type="$VPN_TYPE"

    # Validate the combination exists
    local new_key="${new_provider}:${new_vpn_type}"
    if [[ -z "${PROVIDER_CREDS[$new_key]:-}" ]]; then
        error "$new_provider does not support $new_vpn_type" "exit"
    fi

    # --- Countries ---
    local new_countries
    new_countries=$(prompt_countries "$COUNTRIES")

    # --- Free Only ---
    local new_free_only="$FREE_ONLY"
    if [[ "$FREE_ONLY_PROVIDERS" == *"$new_provider"* ]]; then
        new_free_only=$(ui_choose --header "Free Only (current: $FREE_ONLY)" \
            "off" "on" "keep current")
        [[ "$new_free_only" == "keep current" ]] && new_free_only="$FREE_ONLY"
    else
        new_free_only="off"
    fi

    # --- Handle credential changes ---
    local old_key="${orig_provider}:${orig_vpn_type}"
    local old_cred="${PROVIDER_CREDS[$old_key]:-}"
    local new_cred="${PROVIDER_CREDS[$new_key]:-}"
    WG_ADDR_RESULT="${WIREGUARD_ADDRESSES:-}"

    if [[ "$old_cred" != "$new_cred" ]]; then
        # Credential type changed — must re-enter credentials
        warn "Credential type changed ($old_cred -> $new_cred)."
        warn "Old secrets will be removed."
        echo ""
        local old_needed
        old_needed=$(secrets_needed "$orig_provider" "$orig_vpn_type")
        for secret_name in $old_needed; do
            secret_remove "$secret_name"
        done
        prompt_and_store_credentials "$new_provider" "$new_vpn_type"
    else
        # Same credential type — offer to update credentials
        if ui_confirm "Update credentials?"; then
            prompt_and_store_credentials "$new_provider" "$new_vpn_type"
        fi
    fi

    # Save updated config
    save_container_config "$new_provider" "$new_vpn_type" "$new_countries" \
        "$new_free_only" "$TZ" "$PUID" "$PGID" "$WG_ADDR_RESULT" "${GLUETUN_API_KEY:-}"
    success "Configuration saved"

    # Recreate containers
    echo ""
    if ui_confirm "Recreate containers with new config?"; then
        remove_containers
        create_containers
        podman stop qbittorrent gluetun 2>/dev/null || true
        echo ""
        success "Containers recreated."
        echo -e "  Run ${C_CYAN}torrent start${C_NC} to start the stack."
    else
        info "Config saved. Containers will use new config on next recreation."
    fi
}

# ===================
# update
# ===================
cmd_update() {
    if ! load_container_config; then
        error "No configuration found. Run 'torrent-container install' first." "exit"
    fi

    echo ""
    info "Pulling latest images..."

    local old_gluetun new_gluetun old_qbt new_qbt
    old_gluetun=$(podman image inspect --format '{{.Id}}' \
        "$IMAGE_GLUETUN" 2>/dev/null || echo "none")
    old_qbt=$(podman image inspect --format '{{.Id}}' \
        "$IMAGE_QBITTORRENT" 2>/dev/null || echo "none")

    podman pull "$IMAGE_GLUETUN"
    podman pull "$IMAGE_QBITTORRENT"

    new_gluetun=$(podman image inspect --format '{{.Id}}' \
        "$IMAGE_GLUETUN" 2>/dev/null || echo "none")
    new_qbt=$(podman image inspect --format '{{.Id}}' \
        "$IMAGE_QBITTORRENT" 2>/dev/null || echo "none")

    local gluetun_updated=false qbt_updated=false
    [[ "$old_gluetun" != "$new_gluetun" ]] && gluetun_updated=true
    [[ "$old_qbt" != "$new_qbt" ]] && qbt_updated=true

    if [[ "$gluetun_updated" == false && "$qbt_updated" == false ]]; then
        success "All images are up to date."
        return 0
    fi

    [[ "$gluetun_updated" == true ]] && success "Gluetun: new image pulled"
    [[ "$qbt_updated" == true ]] && success "qBittorrent: new image pulled"

    echo ""
    warn "Containers must be recreated to use the new images."
    ui_confirm "Recreate containers now?" || {
        info "Skipped. Run 'torrent-container update' again when ready."
        return 0
    }

    # Stop & remove
    remove_containers

    # Recreate from config
    create_containers

    # Stop — user starts with torrent start
    podman stop qbittorrent gluetun 2>/dev/null || true

    echo ""
    success "Containers recreated with new images."
    echo -e "  Run ${C_CYAN}torrent start${C_NC} to start the stack."
}

# ===================
# delete
# ===================
cmd_delete() {
    ui_header "Delete Torrent Containers"

    warn "This will remove:"
    echo "  - gluetun and qbittorrent containers"
    echo "  - Podman secrets (VPN credentials)"
    echo "  - Container configuration file"
    echo ""
    echo -e "  ${C_DIM}qBittorrent config and downloads are NOT removed${C_NC}"
    echo ""

    ui_confirm "Are you sure?" || { info "Cancelled."; return 0; }

    # Stop and remove containers
    remove_containers

    # Remove all torrent secrets
    secrets_remove_all

    # Remove config file
    rm -f "$CONTAINER_CONF"

    echo ""
    success "All container resources deleted"
}

